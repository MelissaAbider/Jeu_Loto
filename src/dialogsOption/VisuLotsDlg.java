    public void initLot(ListeLot lstL) {
        int taille = lstL.getLstL().size();
        this.PanPhotosLots.setLayout(new GridLayout((taille / 2) + 1, 2)); // affiche n line sur 2 colonne
        for (Lot l : lstL.getLstL()) {
            if (l.getCategorie().equals("Bon d'achat")) {
                JLabel lab = new JLabel(l.getDescriptif());
                this.PanPhotosLots.add(lab); // ajout du label
            } else {
                Objet obj = (Objet) l; // convertion de type lot en type Objet
                PanneauImage pan = new PanneauImage(obj.getPho()); // creation de pannel pour la photo avec le phot du
                                                                   // lot courant
                this.PanPhotosLots.add(pan);
            }
        }
    }

